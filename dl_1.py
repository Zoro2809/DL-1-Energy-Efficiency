        # Exponential decay
        lr = initial_lr * np.exp(-decay_rate * epoch)

        # Forward
        Z1 = np.dot(X_train_scaled, W1) + b1
        A1 = 1 / (1 + np.exp(-np.clip(Z1, -500, 500)))
